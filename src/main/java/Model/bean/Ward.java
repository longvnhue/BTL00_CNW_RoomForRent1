package Model.bean;

public class Ward {
	private String id_ward;
	private String id_district;
	private String name_ward;
	
	public String getId_ward() {
		return id_ward;
	}
	public void setId_ward(String id_ward) {
		this.id_ward = id_ward;
	}
	public String getId_district() {
		return id_district;
	}
	public void setId_district(String id_district) {
		this.id_district = id_district;
	}
	public String getName_ward() {
		return name_ward;
	}
	public void setName_ward(String name_ward) {
		this.name_ward = name_ward;
	}
	
}
